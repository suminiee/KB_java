package assign2.exam06;

public class Calculator {
    void powerOn() {
        System.out.println("전원을 켭니다.");
    }

    int plus(int a, int b) {
        return a + b;
    }

    double divide (int x, int y) {
        return (double) x / y;
    }

    void powerOff() {
        System.out.println("전원을 끕니다.");
    }
}
